// Function to display T&Cs in a modal
function displayTermsModal(type = 'user') {
  const terms = type === 'owner' ? TERMS_AND_CONDITIONS.ownerTerms : TERMS_AND_CONDITIONS.userTerms;
  const cabinPolicy = type === 'owner' ? TERMS_AND_CONDITIONS.cabinDesignPolicy : null;
  
  let content = `
    <div id="termsModal" class="terms-modal" style="
      position: fixed;
      top: 0;
      left: 0;
      right: 0;
      bottom: 0;
      background: rgba(0,0,0,0.5);
      display: flex;
      align-items: center;
      justify-content: center;
      z-index: 9999;
      overflow: auto;
    ">
      <div class="terms-content" style="
        background: white;
        border-radius: 8px;
        max-width: 800px;
        width: 90%;
        max-height: 90vh;
        overflow-y: auto;
        padding: 30px;
        box-shadow: 0 10px 40px rgba(0,0,0,0.2);
      ">
        <div style="display: flex; justify-content: space-between; align-items: start; margin-bottom: 20px;">
          <h2 style="margin: 0; color: #333;">${terms.title}</h2>
          <button onclick="document.getElementById('termsModal').remove()" style="
            background: none;
            border: none;
            font-size: 28px;
            cursor: pointer;
            color: #999;
          ">&times;</button>
        </div>
  `;
  
  terms.sections.forEach(section => {
    content += `
      <div style="margin-bottom: 20px;">
        <h3 style="color: #667eea; font-size: 16px; margin: 15px 0 8px 0;">${section.heading}</h3>
        <p style="color: #555; line-height: 1.6; margin: 0; font-size: 14px;">${section.content}</p>
      </div>
    `;
  });
  
  if (cabinPolicy) {
    content += '<hr style="margin: 30px 0; border: 1px solid #eee;"><h2 style="color: #333; margin-bottom: 20px;">' + cabinPolicy.title + '</h2>';
    cabinPolicy.sections.forEach(section => {
      content += `
        <div style="margin-bottom: 20px;">
          <h3 style="color: #667eea; font-size: 16px; margin: 15px 0 8px 0;">${section.heading}</h3>
          <p style="color: #555; line-height: 1.6; margin: 0; font-size: 14px;">${section.content}</p>
        </div>
      `;
    });
  }
  
  content += `
    <div style="margin-top: 30px; text-align: center;">
      <button onclick="document.getElementById('termsModal').remove()" style="
        background: linear-gradient(135deg, #667eea 0%, #764ba2 100%);
        color: white;
        border: none;
        padding: 12px 30px;
        border-radius: 6px;
        cursor: pointer;
        font-weight: bold;
      ">Close Terms & Conditions</button>
    </div>
      </div>
    </div>
  `;
  
  document.body.insertAdjacentHTML('beforeend', content);
}

// Function to validate T&C agreement
function validateTermsAgreement(checkboxId) {
  const checkbox = document.getElementById(checkboxId);
  if (!checkbox || !checkbox.checked) {
    alert('You must agree to the Terms and Conditions to continue');
    return false;
  }
  return true;
}
